numero = float(input("Digite um número positivo e maior que zero: "))


if numero > 0:
    quadrado = numero ** 2
    cubo = numero ** 3
    print(f"Número ao quadrado: {quadrado}")
    print(f"Número ao cubo: {cubo}")
else:
    print("Número inválido. O número deve ser positivo e maior que zero.")




